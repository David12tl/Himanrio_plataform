                <h1 style="text-align: center; color: white; margin-bottom: 40px;">
                    Introducción al Himnario “Sólo a Dios la gloria”
                </h1>

                <p style="text-align: justify; margin-bottom: 20px;">
                    El canto es tan antiguo como la creación, «cuando alababan todas las estrellas del alba, y se regocijaban todos los hijos de Dios».<sup>1</sup> A través de toda la historia el pueblo de Dios ha querido expresarle sus más profundos sentimientos, y nada mejor que por el medio del canto. Dios lo ordena: «Cantad alegres a Dios, habitantes de toda la tierra».<sup>2</sup>
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    Están los ejemplos en la Biblia donde el canto es parte integral de la adoración; es en sí un acto de adoración. Con él respondemos a la gracia divina, comunicamos a Dios nuestra alabanza y amor, afirmamos unos a otros nuestra fe y proclamamos ante el mundo las verdades del evangelio.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    El nuevo Himnario Evangélico Presbiteriano <strong>“Sólo a Dios la gloria”</strong> ha sido preparado precisamente con el fin de proveer a las iglesias cristianas, y en especial a aquellas que están dentro de la tradición reformada, el mejor recurso posible para su uso en el canto congregacional.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    Los himnos y cantos del himnario respiran un evidente celo por el evangelio y por la pureza doctrinal. El himnario es teocéntrico en su orientación, de contenido eminentemente bíblico, y enfocado para guiar a los adoradores hacia el tipo de adoración que a Dios más le agrada.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    <strong>“Sólo a Dios la gloria”</strong> es el título del nuevo himnario, pues es lema de nuestro diario quehacer, y en especial de nuestro culto a Dios, tanto en privado como en público. En consecuencia, el himnario sigue las pautas del Catecismo Menor de Westminster que por respuesta a la primera pregunta, ¿Cuál es el fin principal del hombre? afirma: «El fin principal del hombre es el de glorificar a Dios y gozar de él para siempre.»
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    Aun siendo dos los enunciados, con himnos y cantos en cada rubro: primero, glorificamos a Dios por todo lo que él es, y por lo que en su gracia ha hecho por nuestra redención en Cristo su amado Hijo; y segundo, habiendo el Santo Espíritu aplicado a nuestros corazones la redención comprada por Cristo, gozamos de la vida en Cristo, de la comunión con Cristo y del servicio por la causa de Cristo, así en el hogar, en la sociedad y hasta en la gloria; con todo, reconocemos que no son dos fines, sino que en conjunto expresan el único y solo fin principal del ser humano: <strong>Soli Deo Gloria</strong>.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    <strong>“Sólo a Dios la gloria”</strong> es una colección de más de 700 himnos reunidos, algunos de la historia antigua, otros del rico acervo de la himnología tradicional evangélica, y los más de ese vasto y nuevo mundo que hoy por hoy nos ofrece la música religiosa contemporánea.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    Se ha dado cierta preferencia a obras de autores latinoamericanos –incluso a los de nuestra propia Iglesia Nacional Presbiteriana de México– a fin de ofrecer cantos en un lenguaje musical que mejor exprese la cultura e idiosincrasia propias de nuestra iglesia nacional. No olvidamos tampoco que nuestros antepasados en la fe cantaron del salterio, por lo que por primera vez incluimos en esta edición, una colección de 50 cantos basados en los Salmos.
                </p>

                <p style="text-align: justify; margin-bottom: 20px;">
                    En lo musical, hemos bajado los tonos de muchos himnos para facilitar el canto congregacional. Se han hecho arreglos más sencillos para muchos de los himnos. En la edición de música se dan los acordes para guitarra. En algunos casos también se da la indicación de cuáles himnos y cantos se pueden entonar en forma seguida.
                </p>

                <h2 style="color: white; margin-top: 40px;">Recomendaciones prácticas</h2>

                <ul style="margin-bottom: 30px;">
                    <li style="margin-bottom: 15px;">
                    <strong>A ti, pastor:</strong> Dios te ha llamado a ser líder del culto. Eres predicador, administrador, educador y consejero. Pero todo se conjuga en el momento en que te presentas al frente de tu rebaño para llevarlo ante la presencia de Dios. Recuerda que el culto a Dios es nuestra más alta vocación. Hallarás en <strong>“Sólo a Dios la gloria”</strong> una magnífica ayuda que equipa a la iglesia para adorar.
                    </li>
                    <li style="margin-bottom: 15px;">
                    <strong>A ti, instrumentista:</strong> Dios te ha dado el privilegio de ser un apoyo del culto. Cuando tocas el órgano, el piano, la guitarra o cualquier otro instrumento, estás ayudando al pueblo a adorar a Dios. Practica con anticipación y sensibilidad espiritual para que tu música sea instrumento de edificación.
                    </li>
                    <li style="margin-bottom: 15px;">
                    <strong>A ti, hermano(a) miembro de la iglesia:</strong> Dios te ha llamado a adorarlo «en espíritu y en verdad».<sup>3</sup> La adoración es activa, no pasiva. Uno de los lugares del culto donde más participa la congregación es en el canto. Este himnario es tu guión para glorificar a Dios con entendimiento y devoción.<sup>4</sup>
                    </li>
                </ul>

                <p style="text-align: justify; margin-bottom: 20px;">
                    El objetivo central de quienes hemos trabajado en las varias comisiones para que este nuevo Himnario Evangélico Presbiteriano <strong>“Sólo a Dios la gloria”</strong> pueda ver la luz del día, ha sido siempre el de ofrecer al pueblo evangélico, en este nuevo milenio, una colección actualizada de «salmos, himnos y cantos espirituales» con los que podamos glorificar a Dios «cantando con gracia al Señor en nuestros corazones».<sup>5</sup>
                </p>

