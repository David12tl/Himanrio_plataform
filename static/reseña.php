                    <h1>RESEÑA DE LA IGLESIA NACIONAL PRESBITERIANA DE MÉXICO</h1>
                    <br>
                    <h3>PASAJE BASE: Daniel 2.20-22</h3>
                    <br>
                    <p>Dios es el Señor de la Historia, ya que él, por ser SOBERANO (despota), gobierna todo, como dice el texto: «Él muda los tiempos y las edades; quita reyes, y pone reyes…»</p>
                    <br>
                    <h2 style="text-align: center;">EN CUANTO AL NOMBRE</h2>
                    <br>
                    <p style="text-align: center;">Sabemos que somos: IGLESIA, porque el término procede de dos vocablos
                        griegos, de Ek y KALEO (los llamados fuera).
                        Somos NACIONAL, porque nos sostenemos, propagamos y dirigimos con
                        elementos nacionales.
                        Somos PRESBITERIANA, por nuestro Sistema de Gobierno de Presbíteros
                        (ancianos) y REFORMADA, por nuestro Sistema Doctrinal.
                        Nuestra Iglesia fue registrada en Gobernación el 9 de octubre de 1956 y
                        como Asociación Religiosa tenemos el Registro Constitutivo SGAR/126/93,
                        DEL 24 DE MAYO DE 1993.
                        Como fecha oficial de la iniciación de los trabajos en nuestra patria se ha
                        fijado el 1º de octubre del año 1872.
                    </p>
                    <br>
                    <h2 style="text-aling: center;">«SINOPSIS HISTÓRICA»</h2>
                    <br><p>Conviene considerar el origen y desarrollo de nuestra iglesia, desde 6 RUBROS:</p>
                    <br><p><b>1. PRECURSORES DEL PRESBITERIANISMO Y PRIMEROS MISIONEROS PRESBITERIANOS</b>, que comprende de los años 1857 a 1872. La historia de nuestra Iglesia es gloriosa y principia a raíz de la Promulgación de la
                        Constitución de 1857, después de la Guerra de los Tres Años, entre el Partido
                        Liberal y el Reaccionario, que culminó con las Leyes de Reforma, las cuales
                        garantizaban la Libertad de cultos en el país y la separación entre iglesia y el
                        estado, además de la nacionalización de los bienes eclesiásticos. Fue una época
                        gloriosa para el protestantismo, ya que podemos recordar la frase del Presidente de México, que en aquel entonces era Don Benito Juárez, quien dijo:
                        «Del desarrollo del protestantismo depende la futura felicidad del país.»
                        Se pueden considerar como precursores del presbiterianismo al Dr. Julio
                        Mallet Prevost, Sr. W. G. Allen, la Srta. Melinda Rankin, Rev. A. J. Park
                        y al Pbro. Arcadio Morales.
                        LOS PRIMEROS MISIONEROS PRESBITERIANOS que llegaron a México
                        en 1872 fueron los Reverendos: Maxwell Phillips, H. C. Thomson, P. E. Pilkin,
                        M. N. Hutchinson, Tomás F. Wallace y Antonio T. Graybill. Posteriormente
                        se unieron los Reverendos J. Milton Greene, Hubert W. Brown, Guillermo
                        Wallace, Samuel T. Wilson y Guillermo A. Ross. Pero no podemos pasar desapercibidos a GRANDES MUJERES, en su mayoría, esposas de Pastores que trabajaron hombro con hombro en la evangelización en México, tal es el caso de:
                        Ana Virginia Greene, Wilma J. Brawn, Elizabeth M. Wallace, Anita R. Boyce,
                        Tabitha Petran, Z.V. Myers, Juana Lorena Ross, J. O. Shelby e Isabella B. Brown.
                        Tampoco podemos olvidar que nuestra Iglesia ha tenido sus mártires.
                        </p>
                    <br><p><b>2. INICIO DE LA IGLESIA PRESBITERIANA EN MÉXICO Y PRIMEROS
                         PRESBITERIOS DE MÉXICO</b>, de 1872 a 1901. Debemos tomar en cuenta
                        que estaban trabajando dos Misiones Presbiterianas: la del norte, cuyos primeros misioneros desembarcaron en Veracruz el 1º de octubre de 1872, y
                        la Misión del Sur, que llegó por el norte de nuestra República, Brownsville y
                        Matamoros, por el año 1873.</p>
                        <br>
                        <p><b>El primer Presbiterio</b> que se organizó fue el de Zacatecas el 31 de mayo de
                           1883, en la Ciudad de Zacatecas. Este Presbiterio abarcaba los estados de Zacatecas, San Luis Potosí, Aguascalientes, Coahuila y parte de Nuevo León
                        </p>
                        <br>
                        <p>
                           <b>El segundo Presbiterio</b>fue el de Tamaulipas, bajo los auspicios de la Misión
                            del Sur, el 21 de abril de 1884, y comprendió parte del estado de Nuevo León
                            y Tamaulipas. Este Presbiterio alcanzó pronto su autonomía, por lo cual ya no
                            recibieron ayuda de la Misión del Sur.
                        </p>
                        <br>
                        <p>
                            <b>El tercer Presbiterio</b> fue el de la Ciudad de México, organizado el 1º de mayo
                            de 1885. Su radio de acción fueron los estados de México, Guerrero, Michoacán, Morelos, Veracruz y Distrito Federal.
                        </p>
                        <br>
                        <p> <b>El cuarto Presbiterio</b>fue el de «El Golfo de México», el 8 de mayo de 1886,
                            y su campo abarcó los estados de Yucatán, Campeche y Chiapas.
                        </p>
                        <br>
                        <p>
                            <b>3. ORGANIZACIÓN DEL PRIMER SÍNODO HASTA EL PLAN DE CINCINNATI</b>, de 1901 a 1919. ¿Cómo surgió la idea? El Rev. Hubert W. Brown
                            predicó el sermón de apertura en el Presbiterio Nacional de la Ciudad de
                            México, ocupándose del mismo asunto que trató en una editorial; elaboró un
                            plan y pidió al Presbiterio que hiciera algo si creía que era el tiempo oportuno.
                            Debido a esa iniciativa, el nuevo Presidente, Rev. Arcadio Morales, publicó el
                            15 de mayo de 1899 la iniciativa para organizar el Sínodo General, creyendo
                            que era tiempo de tal acción, por lo cual formuló e indicó los pasos a seguir:
                        </p>
                        <br>
                        <p>
                            La Organización se llevó a efecto en la Iglesia «El Divino Salvador» de
                            la Ciudad de México, iniciando las actividades de la reunión el 6 de julio
                            de 1901, donde predicó el Rev. Charles C. Millar. Se nombró un presidente
                            interino, recayendo tal cargo en el Rev. Leandro Garza Mora, pidiendo a los
                            32 ministros y a los Ancianos representantes, que ratificaran su deseo de organizar el Sínodo, y acto seguido, el 8 de julio de 1901, a las 10:15 horas, el
                            Sínodo quedó conformado. <br>
                            La Primera Mesa Directiva del Sínodo quedó integrada de la siguiente manera: Presidente Rev. Arcadio Morales; Vicepresidente H. W. Brown; Secretario permanente Rev. Leandro Garza Mora y Secretario temporal Rev. Alfonso
                            Herrera. <br>
                            Por fin, el 9 de julio de 1901, después de aprobar el acta de Organización,
                            el Presidente declaró: «En nombre de las Iglesias, de los Consistorios, de los
                            Presbiterios y con autorización de las Asambleas Presbiterianas, declaro insta-
                            lado para siempre el Sínodo de la Iglesia Presbiteriana en México.»
                        </p>
                        <h2>4. Del Plan de Cincinnati a la Organización de la R. Asamblea General (1919–1947)</h2>
                        <p>Pasaron escasamente 7 años de la organización del Sínodo General cuando en EE.UU., las misiones decidieron dividir México en 8 regiones para organizar el trabajo evangelístico. Esto implicaba aceptar nuevas formas de gobierno, doctrina y liturgia, lo cual fue rechazado por muchos presbiterianos.</p>
                        
                        <h3>Distribución de regiones por misión:</h3>
                        <ul>
                        <li><strong>Iglesia Presbiteriana del Sur (E.U.):</strong> Michoacán, Guerrero, parte del Edo. de México, parte de Morelos y el D.F.</li>
                        <li><strong>Iglesia Presbiteriana del Norte (E.U.):</strong> Oaxaca, Chiapas, Tabasco, Campeche, Sur de Veracruz y Quintana Roo.</li>
                        <li><strong>Iglesia Presbiteriana Asociada y Reformada:</strong> Parte de San Luis Potosí y Norte de Veracruz.</li>
                        <li><strong>Iglesia Episcopal del Norte:</strong> Guanajuato, Querétaro, Hidalgo, Tlaxcala, Puebla, parte del Edo. de México y parte de Morelos.</li>
                        <li><strong>Iglesia de los Discípulos:</strong> Zacatecas y parte de San Luis Potosí.</li>
                        <li><strong>Amigos (Cuáqueros):</strong> Tamaulipas.</li>
                        <li><strong>Iglesia Metodista del Sur:</strong> Chihuahua, Durango, Coahuila y Nuevo León.</li>
                        <li><strong>Iglesia Congregacional:</strong> Sonora, Sinaloa, Nayarit, Jalisco y las Bajas Californias.</li>
                        </ul>

                        <p>En 1944 se formó el Sínodo del Golfo, seguido por el Presbiterio Peninsular. En 1947 se organizaron nuevos presbiterios y el Sínodo Nacional del Centro. Finalmente, el 28 de noviembre de 1947 se organizó la R. Asamblea General en la Iglesia «El Divino Salvador» en el Centro de la Ciudad de México.</p>
                    </section>

                    <section>
                        <h2>5. De la Organización de la R. Asamblea General al Centenario (1947–1972)</h2>
                        <p>La primera Mesa Directiva fue presidida por el Pbro. José Coffin. Surgieron conflictos por el Plan de Cincinnati y por la fundación del C.M.I. y C.I.I.C. en 1948. Esto provocó divisiones entre pastores y la disolución del Presbiterio Nacional de la Ciudad de México, que eventualmente formó la Iglesia Presbiteriana Conservadora A.R.</p>
                        <p>La Iglesia Nacional Presbiteriana siguió creciendo, especialmente en Chiapas, hasta llegar al Centenario en 1972.</p>
                    </section>
                      <br>
                    <section>
                        <h2>6. Del Centenario a la Actualidad (1972–2011)</h2>
                        <p>Se acordó el retiro de las misiones, dejando en manos mexicanas las propiedades y responsabilidades. Se creó el <strong>Movimiento de Avance Presbiteriano</strong> (1991–1997) con el lema:</p>
                        <blockquote><strong>«Siguiendo la verdad en amor, crezcamos en todo»</strong></blockquote>
                        <p>El objetivo fue el crecimiento integral para 1997, con la meta de triplicar membresía, oficiales, pastores, misioneros y estudiantes.</p>
                        <p>Actualmente, la Iglesia cuenta con <strong>13 Sínodos</strong> y <strong>68 Presbiterios</strong> en todo México.</p>
                    </section>

